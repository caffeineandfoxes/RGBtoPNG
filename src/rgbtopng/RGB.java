package rgbtopng;

import java.awt.Color;

/**
 *
 * @author david
 */
public class RGB {
    int red, green, blue;
    
    public RGB()    {
        this.red = 0;
        this.green = 0;
        this.blue = 0;
    }
    
    public RGB(int red, int green, int blue)    {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }

    public int getRed() {
        return red;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int green) {
        this.green = green;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }

    @Override
    public String toString() {
        return "" + red + "-" + green + "-" + blue;
    }
    
    public Color toColor()  {
        return new Color(red, green, blue);
    }
}
